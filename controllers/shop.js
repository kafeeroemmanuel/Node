const Product = require("../models/product"); //Import model obj, begins with caps
const Cart = require("../models/cart");

exports.getProducts = (req, res, next) => {
  Product.findAll()
    .then((products) => {
      res.render("shop/product-list", {
        prods: products,
        pageTitle: "All Products",
        path: "/products",
      });
    })
    .catch((err) => console.log(err));
};

exports.getProduct = (req, res, next) => {
  const prodId = req.params.productId;
  Product.findByPk(prodId) //findByd was replaced by findByPk
    .then((product) => {
      res.render("shop/product-detail", {
        product: product,
        pageTitle: "Ema's product details",
        path: "/products",
      });
    })
    .catch((err) => console.log(err));
  //Product.findAll({where:{id:prodId}}).then(()=>{returns an array, so product: product[0]}).catch()
};

